
public class Math {
    public static void main(String[] args) {

        // Find the maximum number using Math.max()
        int maxNum = java.lang.Math.max(5, 10);

        // Print the result
        System.out.println("The maximum number is: " + maxNum);

        // Find the minimum number using Math.max()
        int minNum = java.lang.Math.min(5, 10);

        // Print the result
        System.out.println("The minimum number is: " + minNum);

        // Find the squareroot using Math.sqrt()
        double sqrtNum = java.lang.Math.sqrt(16);

        // Print the result
        System.out.println("The squareroot of 16 is: " + sqrtNum);

        //genetare random number using Math,random()
        System.out.println("generated random number is"+java.lang.Math.random()*100);
    }
}

